package com.anrosoft.jumpinghero.assets;

public class TextEffect {

	public float textEffectPosX;
	public float textEffectPosY;

	public float textEffectSpeedX;
	public float textEffectSpeedY;

	public String textEffectString;

	public TextEffect(float textEffectPosX, float textEffectPosY,
			float textEffectSpeedX, float textEffectSpeedY,
			String textEffectString) {
		super();
		this.textEffectPosX = textEffectPosX;
		this.textEffectPosY = textEffectPosY;
		this.textEffectSpeedX = textEffectSpeedX;
		this.textEffectSpeedY = textEffectSpeedY;
		this.textEffectString = textEffectString;
	}

}
